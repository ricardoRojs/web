<h4>Términos y condiciones</h4>
<p>
    GLOBAL INVESTMENT SOLUTION S.A.C. (abreviado GIS S.A.C.) pone a disposición de sus clientes el servicio de
    pago través de la aplicación “PAY WEB” en línea.
</p>
<p>
    Las transacciones realizadas a través de la WEB son procesadas por CULQI S.A.C. una compañía que proporciona
    servicios de gestión de cobro a GIS S.A.C. De esta manera, CULQI S.A.C. es la única responsable del procesamiento
    de las transacciones efectuadas por los usuarios que adquieran una recarga a través de la WEB.
</p>
<p>
    Solo se aceptan transacciones con Tarjetas Crédito o Débito de las marcas Visa, Master Card, Diners Club o
    American Express, que cuenten con el código de seguridad (CVV).
</p>
<p>
    Las transacciones se realizan con cargo a la Tarjeta de Crédito o Débito del usuario. La plataforma operada
    por CULQI S.A.C está diseñada para aceptar pagos en la moneda dólares americanos (USD).
</p>
<p>
    El usuario debe tener presente que si la cuenta asociada a su tarjeta de crédito o débito es en moneda extranjera
    (dólares), el tipo de cambio utilizado será el que haya dispuesto su respectiva entidad financiera. Asimismo, en
    caso de pagos con Tarjeta de Crédito y Débito Visa, el usuario debe tener presente que algunas tarjetas con código
    CVV podrían ser rechazadas por la plataforma de pago puesta a disposición debido a políticas de seguridad del banco
    emisor. En cualquier caso, sugerimos contactarse con su banco para obtener mayor información.
</p>
<p>
    El Comprobante de Pago se generará una vez realizada la compra. Este comprobante de pago será otorgado vía correo
    electrónico, para lo cual será enviado al Email ingresado por el usuario previamente a la realización del pago.
    Para que se pueda recibir el Comprobante de Pago por esta vía, es importante que se ingrese un correo electrónico
    válido. No se permitirá recargas de saldo sin haber consignado previamente una cuenta de correo electrónico.
    GIS S.A.C. enviará el Comprobante de Pago a la dirección de correo electrónico consignada, por lo que no se
    responsabiliza por cualquier error derivado de consignar una dirección equivocada.
</p>
<p>
    Toda compra realizada por este medio no tiene derecho a cambio, anulación o devolución. Se recomienda que el
    cliente confirme los montos antes de concretar la compra.
</p>
<p>
    El cliente autoriza expresamente a GIS S.A.C. para hacer uso y tratar los datos personales que le proporcione en la
    contratación de servicios, así como de la información que se derive de su uso, incluida aquella que resulte de la
    navegación que el suscrito realice en cualquier sitio web de GIS S.A.C., en adelante la Información.
</p>
